#include <stdio.h>
#include "calc.h"

/**
 * op_add - sum of two nums
 * @a: num 1
 * @b: num 2
 * Return: result
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction of two nums
 * @a: num 1
 * @b: num 2
 * Return: result
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mul of two nums
 * @a: num 1
 * @b: num 2
 * Return: result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - div of two nums
 * @a: num 1
 * @b: num 2
 * Return: result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod of two nums
 * @a: num 1
 * @b: num 2
 * Return: result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
